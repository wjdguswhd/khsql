-- 216번 사원의 이름 출력 (함수 명: SELECT216)
CREATE OR REPLACE FUNCTION SELECT216
RETURN EMPLOYEE.EMP_NAME%TYPE
IS
    NAME EMPLOYEE.EMP_NAME%TYPE;
BEGIN
    SELECT EMP_NAME INTO NAME
    FROM EMPLOYEE
    WHERE EMP_ID=216;
    
    RETURN NAME;
END;
/

VARIABLE NAME216 VARCHAR2(20);
EXEC :NAME216 := SELECT216;

--사번을 입력받아 해당 사원의 보너스가 포함된 연봉을 계산하고 리턴하는 함수 생성: BONUS_CALC

CREATE OR REPLACE FUNCTION BONUS_CALC(V_EMP_ID EMPLOYEE.EMP_ID%TYPE)
RETURN NUMBER
IS
    V_SALARY EMPLOYEE.SALARY%TYPE;
    V_BONUS EMPLOYEE.BONUS%TYPE;
    CALC_SAL NUMBER;
BEGIN
    SELECT SALARY,NVL(BONUS,0)
    INTO V_SALARY, V_BONUS
    FROM EMPLOYEE
    WHERE EMP_ID=V_EMP_ID;
    
    CALC_SAL := (V_SALARY+(V_SALARY*V_BONUS))*12;
    
    RETURN CALC_SAL;
END;
/

VAR VAR_CALC NUMBER;
EXEC :VAR_CALC :=BONUS_CALC('&ID');


SELECT EMP_ID, EMP_NAME, BONUS_CALC(EMP_ID)
FROM EMPLOYEE
WHERE BONUS_CALC(EMP_ID) > 50000000;




