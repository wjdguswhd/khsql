-- 매개변수 있는 프로시저 생성
-- 프로시저 DEL_EMP_ID에 사용자로부터 입력받은 사번이 일치하는 사원을
-- EMPLOYEE_COPY 테이블에서 삭제하는 쿼리 작성

CREATE OR REPLACE PROCEDURE DEL_EMP_ID(V_EMP_ID EMPLOYEE.EMP_ID%TYPE)
IS
BEGIN
    DELETE FROM EMPLOYEE_COPY
    WHERE EMP_ID =V_EMP_ID;
    COMMIT;
END;
/
EXEC DEL_EMP_ID('&ID');

SELECT * FROM EMPLOYEE_COPY;

--사용자가 입력한 사번으로 사원의 이름, 급여, 보너스 조회하는 프로시저 SELECT_EMP_ID 생성

CREATE OR REPLACE PROCEDURE SELECT_EMP_ID(V_EMP_ID EMPLOYEE.EMP_ID%TYPE)
IS
    V_EMP_NAME EMPLOYEE.EMP_NAME%TYPE;
    V_SALARY EMPLOYEE.SALARY%TYPE;
    V_BONUS EMPLOYEE.BONUS%TYPE;
BEGIN
    select emp_name,salary,NVL(bonus,0)
    into V_EMP_NAME,V_SALARY,V_BONUS
    from employee 
    where emp_id= V_EMP_ID;
    
    DBMS_OUTPUT.PUT_LINE(V_EMP_NAME);
    DBMS_OUTPUT.PUT_LINE(V_SALARY);
    DBMS_OUTPUT.PUT_LINE(V_BONUS);
END;
/
EXEC SELECT_EMP_ID('&ID');

SET SERVEROUTPUT ON;

--IN 매개변수 : 프로시저 내부에서 사용하는 변수
--OUT 매개변수 : 프로시저 외부에서 사용되는 변수

CREATE OR REPLACE PROCEDURE SELECT_EMP_ID(
    V_EMP_ID IN EMPLOYEE.EMP_ID%TYPE,
    V_EMP_NAME OUT EMPLOYEE.EMP_NAME%TYPE,
    V_SALARY OUT EMPLOYEE.SALARY%TYPE,
    V_BONUS OUT EMPLOYEE.BONUS%TYPE
)
IS
BEGIN
    SELECT EMP_NAME, SALARY, NVL(BONUS,0)
    INTO V_EMP_NAME, V_SALARY, V_BONUS
    FROM EMPLOYEE
    WHERE EMP_ID = V_EMP_ID;
END;
/

